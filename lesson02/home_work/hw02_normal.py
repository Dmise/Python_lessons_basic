# Задача-1:
# Дан список, заполненный произвольными целыми числами, получите новый список,
# элементами которого будут квадратные корни элементов исходного списка,
# но только если результаты извлечения корня не имеют десятичной части и
# если такой корень вообще можно извлечь
# Пример: Дано: [2, -5, 8, 9, -25, 25, 4]   Результат: [3, 5, 2]

import math

print("Задача 1")
print("Введите через пробел числа")
numbers_str_list = input().split(' ')
numbers_int_list = [int(number) for number in numbers_str_list]
edited_list = []
cur_sqrt = 0
for number in numbers_int_list:
    if number >= 0:
        cur_sqrt = math.sqrt(number)
        if int(cur_sqrt) == float(cur_sqrt):
            edited_list.append(int(cur_sqrt))
print(f"Резульатат: {edited_list}")

