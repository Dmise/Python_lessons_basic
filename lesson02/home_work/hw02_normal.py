# Задача-4: Дан список, заполненный произвольными целыми числами.
# Получите новый список, элементами которого будут: 
# а) неповторяющиеся элементы исходного списка:
# например, lst = [1, 2, 4, 5, 6, 2, 5, 2], нужно получить lst2 = [1, 2, 4, 5, 6]
# б) элементы исходного списка, которые не имеют повторений:
# например, lst = [1 , 2, 4, 5, 6, 2, 5, 2], нужно получить lst2 = [1, 4, 6]


print("\r\nЗадача 4")
print("Введите через пробел целые произвольные числа")
input_list = input().split(' ')
print(f"а) вывожу неповторяющиеся элементы введенного списка {set(input_list)}")
print(f"б) вывожу элементы исходного списка, которые не имеют повторений {set(input_list)}")
elem_dict = {}
for cur_key in input_list:
    if cur_key in elem_dict:
        elem_dict[cur_key] = 2
    else:
        elem_dict[cur_key] = 1
print(elem_dict)    # для проверки
new_list = []
for elem in elem_dict:
    if elem_dict[elem] == 1:
        new_list.append(elem)
print(new_list)
